// Color themes - edit these values to customize for each background type
// COLORS_DARK is used when HasDarkBackground=true (default)
// COLORS_LIGHT is used when HasDarkBackground=false
var COLORS_DARK = map[string]lipgloss.Color{
}

var COLORS_LIGHT = map[string]lipgloss.Color{
}

// Frame represents a single animation frame
type Frame struct {
	Duration time.Duration
	Content  []string
	FgColors map[string]string // Maps "x,y" -> color key
	BgColors map[string]string // Maps "x,y" -> color key
}

// ASCIIMotionModel is the Bubbletea model for the animation
type ASCIIMotionModel struct {
	frames            []Frame
	frameIndex        int
	isPlaying         bool
	loop              bool
	width             int
	height            int
	hasDarkBackground bool
}

type asciiMotionTickMsg time.Time

// NewASCIIMotion creates a new animation model
// Set hasDarkBackground to true for dark terminals, false for light terminals
func NewASCIIMotion(hasDarkBackground bool) ASCIIMotionModel {
	return ASCIIMotionModel{
		frames:            frames,
		frameIndex:        0,
		isPlaying:         true,
		loop:              true,
		width:             80,
		height:            24,
		hasDarkBackground: hasDarkBackground,
	}
}

// NewASCIIMotionWithDefaults creates a new animation model with dark background (default)
func NewASCIIMotionWithDefaults() ASCIIMotionModel {
	return NewASCIIMotion(true)
}

// Init initializes the model
func (m ASCIIMotionModel) Init() tea.Cmd {
	return m.tick()
}

func (m ASCIIMotionModel) tick() tea.Cmd {
	if !m.isPlaying || len(m.frames) == 0 {
		return nil
	}
	return tea.Tick(m.frames[m.frameIndex].Duration, func(t time.Time) tea.Msg {
		return asciiMotionTickMsg(t)
	})
}

// Update handles messages
func (m ASCIIMotionModel) Update(msg tea.Msg) (tea.Model, tea.Cmd) {
	switch msg := msg.(type) {
	case tea.KeyMsg:
		switch msg.String() {
		case "q", "ctrl+c":
			return m, tea.Quit
		}
	case asciiMotionTickMsg:
		if m.isPlaying && len(m.frames) > 0 {
			m.frameIndex++
			if m.frameIndex >= len(m.frames) {
				if m.loop {
					m.frameIndex = 0
				} else {
					m.frameIndex = len(m.frames) - 1
					m.isPlaying = false
					return m, nil
				}
			}
			return m, m.tick()
		}
	}
	return m, nil
}

// getColor returns the appropriate color for the current background mode
func (m ASCIIMotionModel) getColor(colorKey string) lipgloss.TerminalColor {
	if m.hasDarkBackground {
		return COLORS_DARK[colorKey]
	}
	return COLORS_LIGHT[colorKey]
}

// View renders the animation
func (m ASCIIMotionModel) View() string {
	if len(m.frames) == 0 {
		return ""
	}
	frame := m.frames[m.frameIndex]
	var sb strings.Builder

	for y, row := range frame.Content {
		// Convert to runes to get character indices (not byte offsets)
		chars := []rune(row)
		for x, ch := range chars {
			key := fmt.Sprintf("%d,%d", x, y)
			style := lipgloss.NewStyle()
			if fgKey, ok := frame.FgColors[key]; ok {
				style = style.Foreground(m.getColor(fgKey))
			}
			if bgKey, ok := frame.BgColors[key]; ok {
				style = style.Background(m.getColor(bgKey))
			}
			sb.WriteString(style.Render(string(ch)))
		}
		if y < len(frame.Content)-1 {
			sb.WriteString("\n")
		}
	}
	return sb.String()
}

// Play starts or resumes the animation
func (m *ASCIIMotionModel) Play() tea.Cmd {
	m.isPlaying = true
	return m.tick()
}

// Pause stops the animation
func (m *ASCIIMotionModel) Pause() {
	m.isPlaying = false
}

// Restart resets to the first frame
func (m *ASCIIMotionModel) Restart() tea.Cmd {
	m.frameIndex = 0
	return m.tick()
}

// IsPlaying returns whether the animation is playing
func (m ASCIIMotionModel) IsPlaying() bool {
	return m.isPlaying
}

// CurrentFrame returns the current frame index
func (m ASCIIMotionModel) CurrentFrame() int {
	return m.frameIndex
}

// TotalFrames returns the total number of frames
func (m ASCIIMotionModel) TotalFrames() int {
	return len(m.frames)
}

// SetSize updates the width and height of the animation
func (m *ASCIIMotionModel) SetSize(width, height int) {
	m.width = width
	m.height = height
}

